    /**
     * if adminMode toggled, checks the passcode in adminTextField and enables
     * adminMode in the VendingMachine Class. Used to increment drink Quantity
     * @param evt 
     */
    private void plusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_plusButtonActionPerformed
        if (adminButton.isSelected() && !vmObj.isAdminEnabled())
            this.plusAdminEnterVariant();
        if (vmObj.isPasswordChange() && vmObj.isAdminEnabled())
            this.plusPasswordChangeVariant();
        if(vmObj.isItemSelected() && vmObj.isAdminEnabled()){
            this.plusIncrementVariant();
        }    
    }//GEN-LAST:event_plusButtonActionPerformed
    /**
     * variation of the plus button for entering password
     */
    private void plusAdminEnterVariant(){
        if (!vmObj.isAdminEnabled()) {
            String holder = adminDisplay.getText();
            if (vmObj.isCodeEqual(holder)) {
                vmObj.setAdminEnabled(true);
                this.adminDisplay.setText("success");
            }
            else{
                this.adminDisplay.setText("incorrect");
            }
        }
    }
    /**
     * variation of the plus button for changing password
     */
    private void plusPasswordChangeVariant(){
        if(!reEnter){//first entry
            vmObj.setTempPW(adminDisplay.getText());
            this.adminField = reEnterPrompt;
            this.updateAdminDisplay();
            reEnter = true;
        }
        else if(reEnter){//second entry
            if(vmObj.getTempPW().equals(adminDisplay.getText())){//match
                vmObj.setStoredPW();
                reEnter = false;
                vmObj.setPasswordChange(false);
                this.adminField = "";
                adminDisplay.setText("success!");
            }
            else if(!vmObj.getTempPW().equals(adminDisplay.getText())){
                adminField = "";
                adminDisplay.setText("mismatch");
            }    
        }
    }
    /**
     * increments the selected Item
     */
    private void plusIncrementVariant(){
        vmObj.incrementItemQuantity();
        this.setDisplayToQuantity();
    }
