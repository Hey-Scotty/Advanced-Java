    /**
     * can be used to delete a digit of the entered passcode or decrement
     * the quantity of a item if admin mode is toggled
     * @param evt 
     */
    private void minusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minusButtonActionPerformed
        if(!vmObj.isItemSelected())
            this.deleteFromField();
        if(vmObj.isItemSelected() && vmObj.isAdminEnabled()){
            vmObj.decrementItemQuantity();
            this.setDisplayToQuantity();
        }   
    }//GEN-LAST:event_minusButtonActionPerformed
    /**
     * adds a Dollar to the balance
     * @param evt 
     */
    private void addDollarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDollarActionPerformed
        if(!vmObj.isAdminEnabled()){
            vmObj.addDollar();
            this.updateDisplay();
        }
        else if(vmObj.isAdminEnabled()){
            this.changePasswordVariant();
        }           
    }//GEN-LAST:event_addDollarActionPerformed
    /**
     * variant of the addDollar button that activates the password change
     * process if the admin mode has been toggled in the vmObj class
     */
    private void changePasswordVariant(){
        if(!vmObj.isPasswordChange()){
            vmObj.setPasswordChange(true);
            this.adminField = changePrompt;
            this.updateAdminDisplay();
        }
    }
